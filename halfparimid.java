

public class halfparimid {
    public static void main(String args[]){
        int n=5;
        halfparimid(n);
        
    }
    public static int halfparimid(int n){
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                System.out.print("*");
            }
            System.out.println();
        }
        return 0;

    }
    
}
